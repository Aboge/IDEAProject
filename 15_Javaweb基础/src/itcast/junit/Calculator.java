/*
* 计算器类
*
*
* */


public class Calculator {
    /*
    * 加法
    * @param a
    * @param b
    * @return a + b
    * */
    public int add(int a,int b){
        return a + b;
    }

    /*
    * 减法
    * @param a
    * @param b
    * @return a - b
    * */
    public int sub(int a,int b){
        return a + b;
    }
}
